    private void showresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showresActionPerformed
        double n1=Double.parseDouble(this.num1.getText());
        double n2=Double.parseDouble(this.num2.getText());
        double result=0;
        jLabel4.setVisible(false);
        jLabel5.setVisible(false);
        if(suma){
            result=n1+n2;
        }
        if(resta){
            result=n1-n2;
        }
        if(div){
            if(n2!=0){
                result=n1/n2;
            }else{
                jLabel4.setVisible(true);
            }
        }
        if(multi){
            result=n1*n2;
        }
        if(modulo){
            if(n2!=0){
                result=n1%n2;
            }else{
                jLabel5.setVisible(true);
            }
        }
        resultado.setText(""+result);
    }//GEN-LAST:event_showresActionPerformed
    
